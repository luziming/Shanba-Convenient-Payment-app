public class NewsDetailFragment extends BaseFragment {


    @Bind(R.id.myProgressBar)
    ProgressBar webView_progress;
    @Bind(R.id.webView)
    WebView webView;

    @Override
    public View initViews() {
        View view = inflater.inflate(R.layout.fragment_news_detail, null);
        ButterKnife.bind(this, view);
        return view;
    }

    @Override
    public void initData() {
        Bundle arguments = getArguments();
        init(arguments.getString("url"));
    }

    private void init(String url){
        //WebView加载web资源
        webView.loadUrl(url);
        //覆盖WebView默认使用第三方或系统默认浏览器打开网页的行为，使网页用WebView打开
        webView.setWebViewClient(new WebViewClient(){
            @Override
            public boolean shouldOverrideUrlLoading(WebView view, String url) {
                // TODO Auto-generated method stub
                //返回值是true的时候控制去WebView打开，为false调用系统浏览器或第三方浏览器
                view.loadUrl(url);
                return true;
            }
        });

        webView.setWebChromeClient(new WebChromeClient() {
            @Override
            public void onProgressChanged(WebView view, int newProgress) {
                if (newProgress == 100) {
                    webView_progress.setVisibility(View.INVISIBLE);
                } else {
                    if (View.INVISIBLE == webView_progress.getVisibility()) {
                        webView_progress.setVisibility(View.VISIBLE);
                    }
                    webView_progress.setProgress(newProgress);
                }
                super.onProgressChanged(view, newProgress);
            }
        });
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        View rootView = super.onCreateView(inflater, container, savedInstanceState);
        ButterKnife.bind(this, rootView);
        return rootView;
    }

    @Override
    public void onDestroyView() {
        super.onDestroyView();
        ButterKnife.unbind(this);
    }
}
